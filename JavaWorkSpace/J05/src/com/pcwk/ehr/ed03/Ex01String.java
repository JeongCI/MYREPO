package com.pcwk.ehr.ed03;

public class Ex01String {

	public static void main(String[] args) {
		String firstName[] = {"Kim","Park","Lee"};
		
		for(int i=0;i<firstName.length;i++) {
			System.out.printf("firstName[%d]=>%s\n",i,firstName[i]);
		}

		String tmp = firstName[2];
		System.out.println("tmp:"+tmp);
		
		
		//향상된 for
		for(String str :firstName) {
			System.out.println(str);
		}
	}

}
//firstName[0]=>Kim
//firstName[1]=>Park
//firstName[2]=>Lee
//tmp:Lee
