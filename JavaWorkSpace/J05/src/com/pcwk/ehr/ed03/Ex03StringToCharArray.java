package com.pcwk.ehr.ed03;

public class Ex03StringToCharArray {

	public static void main(String[] args) {
		char[] chArray= {'a','b','c','d','e'};
		
		String str = new String(chArray);//char배열 -> String
		
		System.out.println("str:"+str);
		
		for(int i=0;i<str.length();i++) {
			char ch = str.charAt(i);
			System.out.printf("ch[%d]=%c\n",i,ch);
		}

		
		char chNewArray[]= str.toCharArray();//String -> char배열
		System.out.println(chNewArray);
		
	}

}
