package com.pcwk.ehr.ed02;

public class Ex02Quiz {

	public static void main(String[] args) {
		int score[] = {16,11,13,46,50,88};					
		int max = score[0];//최대값
		int min = score[0];//최소값
		
		for(int i=0;i<score.length;i++) {
			
			
			if(score[i]>max) {//최대값보다 크면 최대값을 score[i]로 변경
				max = score[i];
			}
			
			if(score[i]<min) {//최소값보다 작으면 최소값을 score[i]로 변경
				min = score[i];
			}
			
		}
		
		System.out.println("최대값:"+max);
		System.out.println("최소값:"+min);

	}

}
